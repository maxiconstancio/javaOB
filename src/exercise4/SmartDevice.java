package exercise4;

public class SmartDevice {
    String processor;
    String memory;
    String os;
    String brand;
    String model;
    public SmartDevice () {}

    public SmartDevice(String processor, String memory, String os, String brand, String model) {
        this.processor = processor;
        this.memory = memory;
        this.os = os;
        this.brand = brand;
        this.model = model;
    }
}
